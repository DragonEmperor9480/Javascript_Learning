/**
 * Arrow Functions
 * How to create arrow function: To write the arrow function, simply create any variable it can be const, let, 
 * or var but always do prefer with const to avoid unnecessary problems. And then assign the function code to the variable it. 
 * So from now, you can call that function by writing the parenthesis in front of that variable! With arrow function syntax,
 * we consider function as an object and assign the definition to some variable.
 */

const myFunction = () =>{
    let a = 5, b = 4;
    return a+b;
  }
  console.log(myFunction());